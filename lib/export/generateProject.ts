// ─── page.tsx generator ──────────────────────────────────────────────────────

function generatePageTsx(
  sections: ProjectSections,
  variants: ProjectVariants,
  brandName: string,
  heroContent?: { headline?: string; subheadline?: string; ctaLabel?: string },
  ctaContent?:  { heading?: string;  subheading?: string;  ctaLabel?: string },
): string {
  const imports: string[] = [];
  const usages: string[] = [];

  if (sections.navbar) {
    imports.push(`import { Navbar }       from "@/components/sections/Navbar";`);
    usages.push(`      <Navbar brandName="${brandName}" />`);
  }
  if (sections.hero) {
    imports.push(`import { Hero }         from "@/components/sections/Hero";`);
    const heroParts = [`brandName="${brandName}"`];
    if (heroContent?.headline)    heroParts.push(`headline="${heroContent.headline}"`);
    if (heroContent?.subheadline) heroParts.push(`subheadline="${heroContent.subheadline}"`);
    if (heroContent?.ctaLabel)    heroParts.push(`ctaLabel="${heroContent.ctaLabel}"`);
    usages.push(`      <Hero ${heroParts.join(" ")} />`);
  }
  if (sections.features) {
    imports.push(`import { Features }     from "@/components/sections/Features";`);
    usages.push(`      <Features />`);
  }
  if (sections.testimonials) {
    imports.push(`import { Testimonials } from "@/components/sections/Testimonials";`);
    usages.push(`      <Testimonials />`);
  }
  if (sections.pricing) {
    imports.push(`import { Pricing }      from "@/components/sections/Pricing";`);
    usages.push(`      <Pricing />`);
  }
  if (sections.cta) {
    imports.push(`import { CTA }          from "@/components/sections/CTA";`);
    const ctaParts: string[] = [];
    if (ctaContent?.heading)    ctaParts.push(`heading="${ctaContent.heading}"`);
    if (ctaContent?.subheading) ctaParts.push(`subheading="${ctaContent.subheading}"`);
    if (ctaContent?.ctaLabel)   ctaParts.push(`ctaLabel="${ctaContent.ctaLabel}"`);
    usages.push(ctaParts.length ? `      <CTA ${ctaParts.join(" ")} />` : `      <CTA />`);
  }

  void variants; // variants are baked into the component files themselves

  return `// Generated by HoltzUI
${imports.join("\n")}

export default function Page() {
  return (
    <main
      style={{
        backgroundColor: "var(--background)",
        color: "var(--foreground)",
        fontFamily: "var(--font-sans)",
      }}
    >
${usages.join("\n")}
    </main>
  );
}
`;
}

