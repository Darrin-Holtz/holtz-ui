// ─── Main export ─────────────────────────────────────────────────────────────

const defaultVariants: ProjectVariants = {
  navbar: "default",
  hero: "centered",
  pricing: "cards",
  features: "grid",
  testimonials: "grid",
  cta: "centered",
};

export function generateProjectFiles(
  tokens: DesignTokens,
  sections: ProjectSections = { navbar: true, hero: true, features: true, testimonials: true, pricing: true, cta: true },
  brandName = "HoltzUI",
  variants: Partial<ProjectVariants> = {},
  heroContent?: { headline?: string; subheadline?: string; ctaLabel?: string },
  ctaContent?:  { heading?: string;  subheading?: string;  ctaLabel?: string },
): Record<string, string> {
  const v: ProjectVariants = { ...defaultVariants, ...variants };

  const files: Record<string, string> = {
    "app/page.tsx":       generatePageTsx(sections, v, brandName, heroContent, ctaContent),
    "app/layout.tsx":     generateLayoutTsx(tokens),
    "app/globals.css":    generateGlobalsCss(tokens),
    "lib/tokens.ts":      generateTokensTs(tokens),
    "package.json":       generatePackageJson(brandName),
    "next.config.ts":     generateNextConfig(),
    "postcss.config.mjs": generatePostcssConfig(),
    "tsconfig.json":      generateTsConfig(),
    "README.md":          generateReadme(brandName, sections),
  };

  if (sections.navbar)       files["components/sections/Navbar.tsx"]       = generateNavbarComponent(v.navbar, brandName);
  if (sections.hero)         files["components/sections/Hero.tsx"]         = generateHeroComponent(v.hero, brandName);
  if (sections.features)     files["components/sections/Features.tsx"]     = generateFeaturesComponent(v.features);
  if (sections.testimonials) files["components/sections/Testimonials.tsx"] = generateTestimonialsComponent(v.testimonials);
  if (sections.pricing)      files["components/sections/Pricing.tsx"]      = generatePricingComponent(v.pricing);
  if (sections.cta)          files["components/sections/CTA.tsx"]          = generateCTAComponent(v.cta);

  return files;
}
